// Helper functions
export function getRandomAvatar(): string {
  const avatarKeys = Object.keys(avatars).filter(k => k !== 'default')
  const randomKey = avatarKeys[Math.floor(Math.random() * avatarKeys.length)]
  return avatars[randomKey as keyof typeof avatars]
}

export function getQuestMedalByDifficulty(difficulty: 'Easy' | 'Medium' | 'Hard' | 'Expert'): string {
  const mapping = {
    Easy: questMedals.green,
    Medium: questMedals.blue,
    Hard: questMedals.orange,
    Expert: questMedals.purple,
  }
  return mapping[difficulty]
}

export function getBadgeByRarity(rarity: 'Common' | 'Rare' | 'Epic' | 'Legendary' | 'Mythic'): string {
  const mapping = {
    Common: badges.bronzeUser,
    Rare: badges.silverUser,
    Epic: badges.goldUser,
    Legendary: badges.platinumUser,
    Mythic: badges.questConqueror,
  }
  return mapping[rarity]
}

export function getTokenByTier(tier: 'Bronze' | 'Silver' | 'Gold' | 'Platinum'): string {
  const mapping = {
    Bronze: tokenCredits.bronze,
    Silver: tokenCredits.silver,
    Gold: tokenCredits.gold,
    Platinum: tokenCredits.platinum,
  }
  return mapping[tier]
}

